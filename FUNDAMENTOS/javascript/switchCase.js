const pizza = 3

// Condição switch case - utilizado para validar condição e 
// executar maior quantidade de opções de código ou algoritmos
switch (pizza) {
  case 1:
    console.log('Escolheu a pizza PORTUGUESA!')    
    break;

  case 2:
    console.log('Escolheu a pizza TRÊS QUEIJOS!')    
    break;

  case 3:
    console.log('Escolheu a pizza DE CHOCOLATE!')    
    break;

  default:
    console.log('Escolheu a pizza CALABRESA & MUSSARELLA!')    
    break;
}